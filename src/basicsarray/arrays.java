package basicsarray;

public class arrays {
    public static void main(String[] args){
        // Array stores data in Contiguous memory location
        // contiguous = continuous memory location
        // index starts with 0

        //Declaration of Array
        int[] itzArray;
        itzArray = new int[20]; //allocation memory to array

        // we can declare array like this also
        int itzzArray[]; // declaration
        itzzArray = new int[50]; // allocation

        // ek he line ma declaration and allocation
        int age[] = new int[25];


        // package ke name hmesha small letter se hona chahiye.. preferred practice
        // int ke andar by default 0 store rhta hai
        // float ke andar by default 0.0
    }
}
