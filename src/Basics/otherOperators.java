package Basics;

public class otherOperators {
    public static void main(String[] args) {
        // IncrementOperator a++ which means a = a+1
        // Decrement Operator a-- which means a=a-1
        int a = 13;
        int i = a++; // post increment
        // toh phle a ki value assign hogi i ko uske baad value increase krege.. toh i toh same 13 he rh jayga
        System.out.println(i);
        System.out.println(a);
    }
}
